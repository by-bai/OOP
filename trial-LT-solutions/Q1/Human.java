// represents an Human entity with a name and age
// do NOT change this class

public class Human {

    private String name;
    private int age;

    // constructor
    public Human(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // getters
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}